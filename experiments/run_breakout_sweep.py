# experiments/run_breakout_priors_phased.py
"""
Phased training script for Breakout with two priors (base policies):
- base = hand-coded RAM expert ("expert")
- base = LLM-generated "copy" of the hand-coded expert ("llm_copy")

Each variant:
  * builds ALE/Breakout-v5 (obs_type="ram")
  * wraps with ResidualTunnelControlWrapper + BreakoutTunnelFeatureObsWrapper
  * trains PPO in phases; after each phase evaluates on fresh seeds
  * logs raw return and shaped return (environment reward)

We repeat the entire training+eval pipeline for several RANDOM SEEDS (run seeds)
and aggregate across seeds for combined plots.

Saved per-variant-per-seed:
  - logs/breakout_{variant}_seed{run_seed}_phased_episodes.csv
  - logs/breakout_{variant}_seed{run_seed}_phased_summary.csv

Combined plots across variants AND seeds:
  - logs/breakout_two_raw_curve.png
  - logs/breakout_two_total_curve.png
"""

from __future__ import annotations

import os
import copy
from typing import Any, Callable, Dict, List, Literal

import gymnasium as gym
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

from stable_baselines3 import PPO
from stable_baselines3.common.vec_env import DummyVecEnv, VecNormalize

# LLM copy expert (generated by generate_breakout_llm_experts.py)
from breakout_llm_expert_copy import expert_policy as llm_copy_policy

# Your residual tunnel wrappers
from programmatic_policy_learning.approaches.residual_tunnel import (
    BreakoutRAMTracker,
    BreakoutTunnelFeatureObsWrapper,
    ResidualTunnelControlWrapper,
)

BaseType = Literal["expert", "llm_copy"]
ExpertPolicy = Callable[[Any], int]

# -----------------------------
# Config
# -----------------------------
GLOBAL_SEED = 0
N_RUN_SEEDS = 1

ENV_ID = "ALE/Breakout-v5"

# Training phases
NUM_TRAIN_PHASES = 5
TRAIN_STEPS_PER_PHASE = 20_000

# Evaluation
NUM_EVAL_EPISODES = 10
MAX_EVAL_STEPS = 60_000

# PPO params
PPO_KWARGS = dict(
    learning_rate=5e-5,
    n_steps=2048,
    batch_size=512,
    n_epochs=4,
    gamma=0.99,
    gae_lambda=0.95,
    clip_range=0.1,
    ent_coef=0.0,
    vf_coef=0.5,
    max_grad_norm=0.5,
    verbose=1,
    use_sde=True,
    sde_sample_freq=4,
    policy_kwargs=dict(log_std_init=-1.0),
)

# VecNormalize config
VECNORM_KWARGS = dict(
    norm_obs=True,
    norm_reward=True,
    clip_reward=10.0,
)

# -----------------------------
# Base expert (hand-coded)
# -----------------------------
def make_breakout_expert_ram(*, serve_steps: int = 10, deadband_px: float = 2.0) -> ExpertPolicy:
    """RAM expert: serves, then tracks ball_x."""
    NOOP, FIRE, RIGHT, LEFT = 0, 1, 2, 3
    steps = 0
    tracker = BreakoutRAMTracker()

    def reset() -> None:
        nonlocal steps
        steps = 0
        tracker.reset()

    def expert(obs_ram: Any) -> int:
        nonlocal steps
        steps += 1
        if steps <= serve_steps:
            return FIRE

        st = tracker.update(np.asarray(obs_ram))
        if not st.ok or st.paddle_x is None or st.ball_x is None:
            return NOOP

        dx = float(st.ball_x - st.paddle_x)
        if dx > deadband_px:
            return RIGHT
        if dx < -deadband_px:
            return LEFT
        return NOOP

    expert.reset = reset  # type: ignore[attr-defined]
    return expert


def build_base_policy(base_type: BaseType) -> ExpertPolicy:
    if base_type == "expert":
        return make_breakout_expert_ram()
    if base_type == "llm_copy":
        return lambda obs: int(llm_copy_policy(np.asarray(obs)))
    raise ValueError(base_type)


# -----------------------------
# Env factory (single env instance)
# -----------------------------
def make_raw_breakout_env(seed: int, render_mode: str | None = None) -> gym.Env:
    import ale_py  # noqa: F401
    gym.register_envs(__import__("ale_py"))
    env = gym.make(ENV_ID, obs_type="ram", render_mode=render_mode)
    env.reset(seed=seed)
    return env


def make_wrapped_env(seed: int, base_type: BaseType, render_mode: str | None = None) -> gym.Env:
    env = make_raw_breakout_env(seed=seed, render_mode=render_mode)

    tracker = BreakoutRAMTracker()
    base = build_base_policy(base_type)

    def base_np(obs_any: Any) -> int:
        return int(base(np.asarray(obs_any)))

    env_ctrl = ResidualTunnelControlWrapper(
        env,
        base_policy=base_np,
        tracker=tracker,
        gate_px=20.0,
        delta_x_max=6.0,
        cooldown_steps=8,
        serve_steps=10,
        y_brick=60.0,
        edge_band=12.0,
        k_above=0.002,
        k_edge=0.004,
        posthit_window=35,
        k_post_vx=0.01,
        k_post_up=0.02,
        k_u=0.0007,
        debug=True,
    )

    env_feat = BreakoutTunnelFeatureObsWrapper(env_ctrl, tracker)
    return env_feat


# -----------------------------
# Evaluation
# -----------------------------
def evaluate_policy(
    model: PPO,
    train_vecnorm: VecNormalize,
    *,
    base_type: BaseType,
    seed: int,
    num_episodes: int,
    max_steps: int,
) -> List[Dict[str, float]]:
    rows: List[Dict[str, float]] = []
    rng = np.random.default_rng(seed)

    def _eval_env_fn() -> gym.Env:
        s = int(rng.integers(0, 1_000_000))
        return make_wrapped_env(seed=s, base_type=base_type, render_mode=None)

    eval_venv = DummyVecEnv([_eval_env_fn])
    eval_env = VecNormalize(eval_venv, **VECNORM_KWARGS)

    eval_env.obs_rms = copy.deepcopy(train_vecnorm.obs_rms)
    eval_env.ret_rms = copy.deepcopy(train_vecnorm.ret_rms)
    eval_env.training = False
    eval_env.norm_reward = False

    for ep in range(num_episodes):
        obs = eval_env.reset()
        raw_ret = 0.0
        tot_ret = 0.0
        steps = 0

        for _ in range(max_steps):
            a, _ = model.predict(obs, deterministic=True)
            obs, r, done, infos = eval_env.step(a)
            tot_ret += float(r[0])

            info0 = infos[0] if infos and isinstance(infos, (list, tuple)) else {}
            raw_ret += float(info0.get("reward_raw", 0.0))

            steps += 1
            if bool(done[0]):
                break

        rows.append(
            dict(
                episode_idx=float(ep),
                total_reward=float(tot_ret),
                raw_reward=float(raw_ret),
                total_steps=float(steps),
            )
        )

    eval_env.close()
    return rows


# -----------------------------
# Main experiment loop
# -----------------------------
def run_phased_experiment(base_type: BaseType, run_seed: int) -> pd.DataFrame:
    os.makedirs("logs", exist_ok=True)

    episode_csv = f"logs/breakout_{base_type}_seed{run_seed}_phased_episodes.csv"
    summary_csv = f"logs/breakout_{base_type}_seed{run_seed}_phased_summary.csv"
    raw_plot = f"logs/breakout_{base_type}_seed{run_seed}_raw_curve.png"
    total_plot = f"logs/breakout_{base_type}_seed{run_seed}_total_curve.png"

    train_venv = DummyVecEnv([lambda: make_wrapped_env(seed=run_seed, base_type=base_type, render_mode=None)])
    vec_env = VecNormalize(train_venv, **VECNORM_KWARGS)

    model = PPO("MlpPolicy", vec_env, seed=run_seed, **PPO_KWARGS)

    train_vecnorm = model.get_vec_normalize_env()
    assert train_vecnorm is not None

    all_episode_rows: List[Dict[str, float]] = []
    cumulative_timesteps = 0

    print(f"\n=== [{base_type} | seed {run_seed}] Phase 0: no training (t=0) ===")
    eps0 = evaluate_policy(
        model,
        train_vecnorm,
        base_type=base_type,
        seed=run_seed + 123,
        num_episodes=NUM_EVAL_EPISODES,
        max_steps=MAX_EVAL_STEPS,
    )
    for r in eps0:
        r["phase"] = 0
        r["timesteps"] = float(cumulative_timesteps)
        r["run_seed"] = float(run_seed)
    all_episode_rows.extend(eps0)

    for phase in range(1, NUM_TRAIN_PHASES + 1):
        print(f"\n=== [{base_type} | seed {run_seed}] Phase {phase}: train {TRAIN_STEPS_PER_PHASE} ===")
        model.learn(total_timesteps=TRAIN_STEPS_PER_PHASE, progress_bar=True)
        cumulative_timesteps += TRAIN_STEPS_PER_PHASE

        train_vecnorm = model.get_vec_normalize_env()
        assert train_vecnorm is not None

        eps = evaluate_policy(
            model,
            train_vecnorm,
            base_type=base_type,
            seed=run_seed + 123 + phase,
            num_episodes=NUM_EVAL_EPISODES,
            max_steps=MAX_EVAL_STEPS,
        )
        for r in eps:
            r["phase"] = float(phase)
            r["timesteps"] = float(cumulative_timesteps)
            r["run_seed"] = float(run_seed)
        all_episode_rows.extend(eps)

    episodes_df = pd.DataFrame(all_episode_rows)
    episodes_df.to_csv(episode_csv, index=False)
    print(f"\n[{base_type} | seed {run_seed}] Wrote {episode_csv}")

    summary = (
        episodes_df.groupby(["run_seed", "phase", "timesteps"])
        .agg(
            mean_raw=("raw_reward", "mean"),
            std_raw=("raw_reward", "std"),
            mean_total=("total_reward", "mean"),
            std_total=("total_reward", "std"),
            mean_steps=("total_steps", "mean"),
            std_steps=("total_steps", "std"),
        )
        .reset_index()
    )
    summary["variant"] = base_type
    summary.to_csv(summary_csv, index=False)
    print(f"[{base_type} | seed {run_seed}] Wrote {summary_csv}")

    x = summary["timesteps"].values

    plt.figure(figsize=(7, 5))
    plt.errorbar(x, summary["mean_raw"].values, yerr=summary["std_raw"].values, marker="o", capsize=4)
    plt.xlabel("Cumulative training timesteps")
    plt.ylabel("Raw return (mean ± std)")
    plt.title(f"Breakout ({base_type}) raw return vs training, seed={run_seed}")
    plt.tight_layout()
    plt.savefig(raw_plot, dpi=200)
    plt.close()

    plt.figure(figsize=(7, 5))
    plt.errorbar(x, summary["mean_total"].values, yerr=summary["std_total"].values, marker="o", capsize=4)
    plt.xlabel("Cumulative training timesteps")
    plt.ylabel("Total return (shaped) (mean ± std)")
    plt.title(f"Breakout ({base_type}) total return vs training, seed={run_seed}")
    plt.tight_layout()
    plt.savefig(total_plot, dpi=200)
    plt.close()

    vec_env.close()
    return summary


def make_two_plots(all_summary: pd.DataFrame) -> None:
    os.makedirs("logs", exist_ok=True)

    agg = (
        all_summary.groupby(["variant", "phase", "timesteps"])
        .agg(
            mean_raw=("mean_raw", "mean"),
            std_raw=("mean_raw", "std"),
            mean_total=("mean_total", "mean"),
            std_total=("mean_total", "std"),
        )
        .reset_index()
    )

    plt.figure(figsize=(7, 5))
    for variant, sub in agg.groupby("variant"):
        x = sub["timesteps"].values
        y = sub["mean_raw"].values
        yerr = sub["std_raw"].values
        plt.plot(x, y, marker="o", label=variant)
        if not np.all(np.isnan(yerr)):
            plt.fill_between(x, y - yerr, y + yerr, alpha=0.2)
    plt.xlabel("Cumulative training timesteps")
    plt.ylabel("Raw return (mean over seeds)")
    plt.title("Breakout: raw return vs training (expert vs llm_copy)")
    plt.legend()
    plt.tight_layout()
    plt.savefig("logs/breakout_two_raw_curve.png", dpi=200)
    plt.close()
    print("Saved logs/breakout_two_raw_curve.png")

    plt.figure(figsize=(7, 5))
    for variant, sub in agg.groupby("variant"):
        x = sub["timesteps"].values
        y = sub["mean_total"].values
        yerr = sub["std_total"].values
        plt.plot(x, y, marker="o", label=variant)
        if not np.all(np.isnan(yerr)):
            plt.fill_between(x, y - yerr, y + yerr, alpha=0.2)
    plt.xlabel("Cumulative training timesteps")
    plt.ylabel("Total return (shaped) (mean over seeds)")
    plt.title("Breakout: total return vs training (expert vs llm_copy)")
    plt.legend()
    plt.tight_layout()
    plt.savefig("logs/breakout_two_total_curve.png", dpi=200)
    plt.close()
    print("Saved logs/breakout_two_total_curve.png")


def main() -> None:
    all_summaries: List[pd.DataFrame] = []
    variants: List[BaseType] = ["expert", "llm_copy"]

    for run_idx in range(N_RUN_SEEDS):
        run_seed = GLOBAL_SEED + 1000 * run_idx
        print(f"\n########### RUN {run_idx} (run_seed={run_seed}) ###########")
        for v in variants:
            all_summaries.append(run_phased_experiment(v, run_seed=run_seed))

    combined = pd.concat(all_summaries, ignore_index=True)
    make_two_plots(combined)


if __name__ == "__main__":
    main()