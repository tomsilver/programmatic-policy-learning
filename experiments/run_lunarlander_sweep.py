# experiments/run_lander_td3_priors.py
"""
Phased residual learning sweep for LunarLanderContinuous-v3 with 3 priors:
- base = handwritten expert                         ("expert")
- base = zero-thrust expert                          ("zero")
- base = LLM-generated expert                        ("llm")

Like your Pendulum sweep:
  * phase 0 eval (no training)
  * phases 1..N: train for TRAIN_STEPS_PER_PHASE then eval
  * repeat for N_RUN_SEEDS independent run seeds
  * save per-variant-per-seed CSVs + per-seed plots
  * aggregate across seeds per variant for combined plots

Outputs (in logs/):
  - lander_{variant}_seed{run_seed}_phased_episodes.csv
  - lander_{variant}_seed{run_seed}_phased_summary.csv
  - lander_{variant}_seed{run_seed}_reward_curve.png
  - lander_{variant}_seed{run_seed}_contact_curve.png
  - lander_multi_reward_curve.png
  - lander_multi_contact_curve.png
"""

from __future__ import annotations

import os
from typing import Dict, List, Literal

import gymnasium as gym
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
from gymnasium.spaces import Box

from programmatic_policy_learning.approaches.residual_approach import ResidualApproach

# Handwritten expert (adjust import if your path differs)
from programmatic_policy_learning.approaches.experts.lundar_lander_experts import (
    create_manual_lunarlander_continuous_policy,
)

# LLM expert generated by experiments/generate_lunarlander_experts.py
from lander_llm_expert import expert_policy as llm_policy

BaseType = Literal["expert", "zero", "llm"]

# -----------------------------
# Config
# -----------------------------
ENV_ID = "LunarLanderContinuous-v3"

GLOBAL_SEED = 0
N_RUN_SEEDS = 5  # number of independent runs per variant

NUM_TRAIN_PHASES = 20
TRAIN_STEPS_PER_PHASE = 10_000

# Evaluation config
NUM_EVAL_EPISODES = 50
MAX_EVAL_STEPS = 1_000

# Residual backend (TD3 only, as requested)
BACKEND = "sb3-td3"

LOG_DIR = "logs"


def make_lander_env(seed: int | None = None) -> gym.Env:
    env = gym.make(ENV_ID)
    if seed is not None:
        env.reset(seed=seed)
    return env


def build_handwritten_expert(action_space: Box):
    """Your handwritten expert used as base policy (callable obs->action)."""
    return create_manual_lunarlander_continuous_policy(action_space)


def build_zero_expert(action_space: Box):
    """Always outputs zero main engine + zero side engine."""

    class ZeroExpert:
        def act(self, obs):
            return np.zeros(action_space.shape, dtype=np.float32)

    return ZeroExpert()


def build_llm_expert(action_space: Box):
    """Wrap LLM policy into an .act() expert (shape+clip guaranteed)."""

    class LLMExpert:
        def act(self, obs):
            obs_arr = np.asarray(obs, dtype=np.float32)
            act = np.asarray(llm_policy(obs_arr), dtype=np.float32)
            act = np.reshape(act, action_space.shape).astype(np.float32)
            act = np.clip(act, action_space.low, action_space.high).astype(np.float32)
            return act

    return LLMExpert()


def build_residual_approach(run_seed: int, base_type: BaseType) -> ResidualApproach:
    """Construct ResidualApproach with chosen base policy for a given run_seed."""
    env = make_lander_env(seed=run_seed)
    obs_space = env.observation_space
    act_space = env.action_space
    assert isinstance(act_space, Box), "LunarLanderContinuous must have a Box action space."

    if base_type == "expert":
        expert = build_handwritten_expert(act_space)
    elif base_type == "zero":
        expert = build_zero_expert(act_space)
    elif base_type == "llm":
        expert = build_llm_expert(act_space)
    else:
        raise ValueError(f"Unknown base_type: {base_type}")

    def env_factory(instance_num: int) -> gym.Env:
        return make_lander_env(seed=run_seed + instance_num)

    residual = ResidualApproach(
        environment_description=f"Gymnasium {ENV_ID}",
        observation_space=obs_space,
        action_space=act_space,
        seed=run_seed,
        expert=expert,
        env_factory=env_factory,
        backend=BACKEND,
        total_timesteps=TRAIN_STEPS_PER_PHASE,  # per train() call
        lr=1e-3,
        noise_std=0.1,
        verbose=1,
        train_before_eval=False,  # we manually call train() each phase
        train_env_instance=0,
    )

    env.close()
    return residual


def evaluate_policy(
    residual: ResidualApproach,
    seed: int,
    num_episodes: int,
    max_steps: int,
) -> List[Dict]:
    """Evaluate current (base + residual) policy on fresh envs.

    contact_fraction = fraction of timesteps where both legs are contacting the ground.
    Observation:
      [x, y, vx, vy, angle, ang_vel, leg_l, leg_r]
    """
    rng = np.random.default_rng(seed)
    episode_metrics: List[Dict] = []

    for episode_idx in range(num_episodes):
        env_seed = int(rng.integers(0, 1_000_000))
        env = make_lander_env(seed=env_seed)
        obs, info = env.reset()

        residual.reset(np.asarray(obs, dtype=np.float32), info)

        total_reward = 0.0
        steps = 0
        both_contact_steps = 0

        for _ in range(max_steps):
            obs_array = np.asarray(obs, dtype=np.float32)
            leg_l = float(obs_array[6])
            leg_r = float(obs_array[7])
            if leg_l > 0.5 and leg_r > 0.5:
                both_contact_steps += 1

            action = residual.step()
            obs, rew, terminated, truncated, info = env.step(action)

            total_reward += float(rew)
            steps += 1

            done = bool(terminated or truncated)
            residual.update(obs, float(rew), done, info)
            if done:
                break

        env.close()

        contact_fraction = float(both_contact_steps / max(steps, 1))

        episode_metrics.append(
            {
                "episode_idx": episode_idx,
                "total_reward": total_reward,
                "total_steps": steps,
                "contact_fraction": contact_fraction,
            }
        )

    return episode_metrics


def run_phased_experiment(base_type: BaseType, run_seed: int) -> pd.DataFrame:
    """Run phased training + eval for one base_type and one run_seed."""
    os.makedirs(LOG_DIR, exist_ok=True)

    episode_csv = f"{LOG_DIR}/lander_{base_type}_seed{run_seed}_phased_episodes.csv"
    summary_csv = f"{LOG_DIR}/lander_{base_type}_seed{run_seed}_phased_summary.csv"
    reward_plot = f"{LOG_DIR}/lander_{base_type}_seed{run_seed}_reward_curve.png"
    contact_plot = f"{LOG_DIR}/lander_{base_type}_seed{run_seed}_contact_curve.png"

    residual = build_residual_approach(run_seed=run_seed, base_type=base_type)

    all_episode_rows: List[Dict] = []
    cumulative_timesteps = 0

    print(f"\n=== [{base_type} | seed {run_seed}] Phase 0: no residual training ===")
    phase0_eps = evaluate_policy(
        residual,
        seed=run_seed + 123,
        num_episodes=NUM_EVAL_EPISODES,
        max_steps=MAX_EVAL_STEPS,
    )
    for row in phase0_eps:
        row["phase"] = 0
        row["timesteps"] = cumulative_timesteps
        row["run_seed"] = run_seed
    all_episode_rows.extend(phase0_eps)

    for phase in range(1, NUM_TRAIN_PHASES + 1):
        print(
            f"\n=== [{base_type} | seed {run_seed}] Phase {phase}: "
            f"training for {TRAIN_STEPS_PER_PHASE} timesteps ==="
        )
        residual.train()
        cumulative_timesteps += TRAIN_STEPS_PER_PHASE

        phase_eps = evaluate_policy(
            residual,
            seed=run_seed + 123 + phase,
            num_episodes=NUM_EVAL_EPISODES,
            max_steps=MAX_EVAL_STEPS,
        )
        for row in phase_eps:
            row["phase"] = phase
            row["timesteps"] = cumulative_timesteps
            row["run_seed"] = run_seed
        all_episode_rows.extend(phase_eps)

    episodes_df = pd.DataFrame(all_episode_rows)
    episodes_df.to_csv(episode_csv, index=False)
    print(f"\n[{base_type} | seed {run_seed}] Saved episodes -> {episode_csv}")

    summary = (
        episodes_df.groupby(["run_seed", "phase", "timesteps"])
        .agg(
            mean_reward=("total_reward", "mean"),
            std_reward=("total_reward", "std"),
            mean_contact_fraction=("contact_fraction", "mean"),
            std_contact_fraction=("contact_fraction", "std"),
            mean_steps=("total_steps", "mean"),
            std_steps=("total_steps", "std"),
        )
        .reset_index()
    )
    summary["variant"] = base_type
    summary.to_csv(summary_csv, index=False)
    print(f"[{base_type} | seed {run_seed}] Saved summary -> {summary_csv}")

    # Per-seed plots
    x = summary["timesteps"].values

    plt.figure(figsize=(7, 5))
    y = summary["mean_reward"].values
    yerr = summary["std_reward"].values
    plt.errorbar(x, y, yerr=yerr, marker="o", capsize=4)
    plt.xlabel("Cumulative training timesteps (residual)")
    plt.ylabel("Total reward (mean over episodes)")
    plt.title(f"Lander: {base_type}, seed={run_seed} + residual, reward vs training")
    plt.tight_layout()
    plt.savefig(reward_plot, dpi=200)
    plt.close()

    plt.figure(figsize=(7, 5))
    y = summary["mean_contact_fraction"].values
    yerr = summary["std_contact_fraction"].values
    plt.errorbar(x, y, yerr=yerr, marker="o", capsize=4)
    plt.xlabel("Cumulative training timesteps (residual)")
    plt.ylabel("Contact fraction (both legs)")
    plt.title(f"Lander: {base_type}, seed={run_seed} + residual, contact vs training")
    plt.tight_layout()
    plt.savefig(contact_plot, dpi=200)
    plt.close()

    return summary


def make_multi_plots(all_summary: pd.DataFrame) -> None:
    """Combined multi-variant plots (reward + contact), aggregated across run seeds."""
    agg = (
        all_summary.groupby(["variant", "phase", "timesteps"])
        .agg(
            mean_reward=("mean_reward", "mean"),
            std_reward=("mean_reward", "std"),
            mean_contact_fraction=("mean_contact_fraction", "mean"),
            std_contact_fraction=("mean_contact_fraction", "std"),
        )
        .reset_index()
    )

    # Reward multi-curve
    plt.figure(figsize=(7, 5))
    for variant, sub in agg.groupby("variant"):
        x = sub["timesteps"].values
        y = sub["mean_reward"].values
        yerr = sub["std_reward"].values
        plt.plot(x, y, marker="o", label=variant)
        if not np.all(np.isnan(yerr)):
            plt.fill_between(x, y - yerr, y + yerr, alpha=0.2)
    plt.xlabel("Cumulative training timesteps (residual)")
    plt.ylabel("Total reward (mean over run seeds & episodes)")
    plt.title("LunarLanderContinuous: residual learning with different priors (multi-seed)")
    plt.legend()
    plt.tight_layout()
    plt.savefig(f"{LOG_DIR}/lander_multi_reward_curve.png", dpi=200)
    plt.close()
    print(f"Saved combined reward plot -> {LOG_DIR}/lander_multi_reward_curve.png")

    # Contact multi-curve
    plt.figure(figsize=(7, 5))
    for variant, sub in agg.groupby("variant"):
        x = sub["timesteps"].values
        y = sub["mean_contact_fraction"].values
        yerr = sub["std_contact_fraction"].values
        plt.plot(x, y, marker="o", label=variant)
        if not np.all(np.isnan(yerr)):
            plt.fill_between(x, y - yerr, y + yerr, alpha=0.2)
    plt.xlabel("Cumulative training timesteps (residual)")
    plt.ylabel("Contact fraction (mean over run seeds & episodes)")
    plt.title("LunarLanderContinuous: stability vs training for different priors (multi-seed)")
    plt.legend()
    plt.tight_layout()
    plt.savefig(f"{LOG_DIR}/lander_multi_contact_curve.png", dpi=200)
    plt.close()
    print(f"Saved combined contact plot -> {LOG_DIR}/lander_multi_contact_curve.png")


def main() -> None:
    all_summaries: List[pd.DataFrame] = []

    variants: List[BaseType] = ["llm", "expert", "zero"]

    for run_idx in range(N_RUN_SEEDS):
        run_seed = GLOBAL_SEED + 1000 * run_idx
        print(f"\n########### RUN {run_idx} (run_seed={run_seed}) ###########")
        for v in variants:
            all_summaries.append(run_phased_experiment(v, run_seed=run_seed))

    combined = pd.concat(all_summaries, ignore_index=True)
    make_multi_plots(combined)


if __name__ == "__main__":
    main()